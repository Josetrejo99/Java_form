    public void imprimir(){

        try{
conectar = Conectar.getConnection();
String [] alumnos = {"id","Nombre","Apellido","Correo","habilidad","telefono"};

model = new DefaultTableModel(null,alumnos);
sent = conectar.createStatement();
Resulset rs = sent.executeQuery("SELECT * FROM descrip_estudiantes");
String fila []= new String [6];
while (rs.next()) {
    
    fila[0]= rs.getString("id");
        fila[1]= rs.getString("Nombre");
    fila[2]= rs.getString("Apellido");
    fila[3]= rs.getString("Correo");
    fila[4]= rs.getString("habilidad");
    fila[5]= rs.getString("telefono");
model.addRow(fila);
}
tbestudiantes.setModel(model);
        }catch (SQLException e){
    e.printStackTrace();

    }
    }
