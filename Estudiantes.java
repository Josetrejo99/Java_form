private void btnguardarActionPerformed(java.awt.event.ActionEvent evt){
    try{
        PreparedStatement pat = cn.prepareCall("INSERT INTO descrip_estudiantes (Nombre,Apellido,Correo,habilidad,telefono) VALUES (?,?,?,?,?)");
    pat.setString(1, txtNombre.getText());
     pat.setString(2, txtApellido.getText());
      pat.setString(3, txtCorreo.getText());
       pat.setString(4, txthabilidad.getText());
        pat.setString(5, txttelefono.getText());
        int n =pat.executeUpdate();
        if(n>0){
            JOptionPane.showMessageDialog(null,"Datos guardados.");
        }
    } catch (SQLException e){
    JOptionPane.showMessageDialog(null,"error. "+e.getMessage());
    }
    }

// Modificar datos de estudiante
private void btneditarActionPerformed(java.awt.event.ActionEvent evt){
try{

    int fila = tbestudiantes.getSelectedRow();
    String dao = (String)tbestudiantes.getValueAt(fila,0);
PreparedStatement pat = cn.prepareCall("UPDATE descrip_estudiantes SET Nombre=?,Apellido=?,Correo=?,habilidad=?,telefono=? WHERE id=?");
pat.setString(1, txtNombre.getText());
     pat.setString(2, txtApellido.getText());
      pat.setString(3, txtCorreo.getText());
       pat.setString(4, txthabilidad.getText());
        pat.setString(5, txttelefono.getText());
        pat.setString(6,dao);//La llamada SQL se muestra en la tabla
        int n =pat.executeUpdate();
        if(n>0){
            limpiar();
            imprimir();
            JOptionPane.showMessageDialog(null,"Datos modificados.");
        }
}catch (SQLException e){
    JOptionPane.showMessageDialog(null,"error. "+e.getMessage());
    }


}
// Eliminar estudiantes
private void btneliminarActionPerformed(java.awt.event.ActionEvent evt){

    try{
int fila = tbestudiantes.getSelectedRow();
sent = cn.createStatement();
int n = sent.executeUpdate("DELETE FROM  descrip_estudiantes WHERE id= "+tbestudiantes.getValueAt(fila,0));
    if (n > 0) {
        imprimir();
       JOptionPane.showMessageDialog(null,"Datos eliminados."); 
       limpiar();
    }

    } catch (SQLException e){
    JOptionPane.showMessageDialog(null,"error. "+e.getMessage());
    }
} 
